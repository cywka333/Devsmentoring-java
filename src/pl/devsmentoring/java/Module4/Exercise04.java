package pl.devsmentoring.java.Module4;

public class Exercise04 {
    public static void exec_4(){
        // FOR LOOP
        int y = 50;
        for(int i = 100; i >= y; i--){
            System.out.println(i);
        }
        System.out.println("\nWhile loop\n");
        // WHILE LOOP
        int i = 100;
        while (i>=y){
            System.out.println(i);
            i--;
        }
    }
}
